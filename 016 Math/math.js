console.log("=== JavaScript Math Object Demo ===");

        // Math constants
        console.log("Math.PI:", Math.PI); // Pi (3.14159...)
        console.log("Math.E:", Math.E); // Euler's number (2.718...)
        console.log("Math.LN2:", Math.LN2); // Natural logarithm of 2

        // Rounding functions
        console.log("Math.round(4.7):", Math.round(4.7)); // Round to nearest integer
        console.log("Math.ceil(4.2):", Math.ceil(4.2)); // Round up
        console.log("Math.floor(4.7):", Math.floor(4.7)); // Round down
        console.log("Math.trunc(4.9):", Math.trunc(4.9)); // Remove fractional part

        // Absolute value
        console.log("Math.abs(-5):", Math.abs(-5)); // Absolute value

        // Power and square root
        console.log("Math.pow(2, 3):", Math.pow(2, 3)); // 2 raised to the power of 3
        console.log("Math.sqrt(16):", Math.sqrt(16)); // Square root of 16
        console.log("Math.cbrt(27):", Math.cbrt(27)); // Cube root of 27

        // Trigonometry
        console.log("Math.sin(Math.PI / 2):", Math.sin(Math.PI / 2)); // Sine of 90 degrees
        console.log("Math.cos(0):", Math.cos(0)); // Cosine of 0 degrees
        console.log("Math.tan(Math.PI / 4):", Math.tan(Math.PI / 4)); // Tangent of 45 degrees

        // Logarithms
        console.log("Math.log(Math.E):", Math.log(Math.E)); // Natural log of Euler's number
        console.log("Math.log10(100):", Math.log10(100)); // Log base 10 of 100

        // Random numbers
        console.log("Math.random():", Math.random()); // Random number between 0 and 1
        console.log("Random number between 1 and 10:", Math.floor(Math.random() * 10) + 1);

        // Max and Min
        console.log("Math.max(1, 5, 10):", Math.max(1, 5, 10)); // Maximum value
        console.log("Math.min(1, 5, 10):", Math.min(1, 5, 10)); // Minimum value

        // Exponential and logarithmic functions
        console.log("Math.exp(1):", Math.exp(1)); // e^1
        console.log("Math.log1p(0):", Math.log1p(0)); // Natural log of (1 + 0)

        // Hyperbolic functions
        console.log("Math.sinh(0):", Math.sinh(0)); // Hyperbolic sine
        console.log("Math.cosh(0):", Math.cosh(0)); // Hyperbolic cosine
        console.log("Math.tanh(0):", Math.tanh(0)); // Hyperbolic tangent

        console.log("=== End of Demo ===");